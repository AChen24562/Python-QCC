vehicles = ['bike', 'car', 'truck', 'motorcycle', 'helicopter', 'plane', 'boat', 'waterski']
print(vehicles)
print("")

# print the fourth element in the list
print(vehicles[3])
print("")

# alphabetical permanent sort of the list (list order is changed)
vehicles.sort()
print(vehicles)
print("")

# print the fourth element in the list
print(vehicles[3])
print("")

# alphabetical permanent sort of the list in reverse (list order is changed)
vehicles.sort(reverse=True)
print(vehicles)
print("")

# print the fourth element in the list
print(vehicles[3])
print("")
