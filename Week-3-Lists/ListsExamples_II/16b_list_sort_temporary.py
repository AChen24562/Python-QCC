vehicles = ['bike', 'car', 'truck', 'motorcycle', 'helicopter', 'plane', 'boat', 'waterski']
print(vehicles)
print("")

# print a alphabetically sorted copy of the list (this does not change the list)
print(sorted(vehicles))
print("")

# print the list after printing a sorted copy of the list
print(vehicles)
print("")

# print a alphabetically reverse sorted copy of the list (this does not change the list)
print(sorted(vehicles, reverse=True))
print("")

# print the list after printing a sorted copy of the list
print(vehicles)
print("")
